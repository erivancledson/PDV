    public static String gerarCodigo() {
        int maiorValor = 0;
        for (int i = 0; i < Produtos.getCodigo().size(); i++) {
            if (i == 0) {
                maiorValor = Produtos.getCodigo().get(i);
            } else {
                if (maiorValor < Produtos.getCodigo().get(i)) {
                    maiorValor = Produtos.getCodigo().get(i);
                }
            }
        }
        return Integer.toString(maiorValor + 1);
    }

    public static void limpar() {
        tfCodigo.setText(gerarCodigo());
        tfDescricao.setText("");
        tfUnidades.setText("");
        tfPreco.setText("");
        tfCodigo.requestFocus();
    }

