    private void btConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConcluirActionPerformed
        try {
            if (tfCodigo.getText().trim().isEmpty() || tfDescricao.getText().trim().isEmpty() || tfUnidades.getText().trim().isEmpty() || tfPreco.getText().trim().isEmpty()) {
                JOptionPane.showInternalMessageDialog(rootPane, "Por favor preencha todos os campos.");
            } else {
                int codigo = Integer.parseInt(tfCodigo.getText());
                String descricao = tfDescricao.getText();
                int unidades = Integer.parseInt(tfUnidades.getText());
                float preco = Float.parseFloat(tfPreco.getText().replaceAll(",", "."));

                if (getTitle().contains("Adicionar")) {
                    if (Produtos.getCodigo().indexOf(codigo) != -1) {
                        JOptionPane.showInternalMessageDialog(rootPane, "Já existe um produto cadastro com esse código.\nPor favor, escolha outro código.");
                    } else {
                        Produtos.adicionarProduto(codigo, descricao, unidades, preco);

                        int resultado = JOptionPane.showInternalConfirmDialog(rootPane, "Operação realizada com sucesso.\nDeseja adicionar outro produto?");

                        if (resultado == JOptionPane.YES_OPTION) {
                            limpar();
                        } else {
                            dispose();
                        }
                    }
                } else {
                    Produtos.alterarProduto(codigo, descricao, unidades, preco);
                    JOptionPane.showInternalMessageDialog(rootPane, "Operação realizada com sucesso.");
                    dispose();
                    ConsultarProduto.pesquisar();
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showInternalMessageDialog(rootPane, "Falha ao realizar a operação:\n\n" + ex, "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btConcluirActionPerformed

